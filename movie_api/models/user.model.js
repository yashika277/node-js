let mongoose = require("mongoose");

let userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

let userData = mongoose.model("userSchema", userSchema);
module.exports = userData;
